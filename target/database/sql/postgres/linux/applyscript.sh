#!/bin/bash
# params:
# CONF_USER
# CONF_PWD
# CONF_DBHOST
# CONF_DBPORT
# CONF_DBNAME

P_SQLFILE=$1
P_LOGFILE=$2

export PGPASSWORD="$CONF_PWD"
XPORT=
if [ "$CONF_DBPORT" != "" ]; then
	XPORT=" -p $CONF_DBPORT"
fi

psql -A -a -t -d $CONF_DBNAME -h $CONF_DBHOST $XPORT -U $CONF_USER < $P_SQLFILE > $P_LOGFILE 2>&1
